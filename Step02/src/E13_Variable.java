
public class E13_Variable {

	public static void main(String[] args) {
		//통합 자료형 var
		// 데이터 타입은 컴파일러가 초기화하는 데이터를 보고 결정
		var num = 100;
		System.out.println(num);
//		num = true;

		var flag = true;
//		flag = 1;
		System.out.println(flag);

		var str = "Hello World";
		System.out.println(str);
	}

}
