
public class E26_Array2D {

	public static void main(String[] args) {
		//정수형 배열 3행 4열짜리 생성
		
		int[][] arr = new int[3][4];
		
		System.out.println("행 개수 : " + arr.length);
		System.out.println("0번행의 개수 : " + arr[0].length);
		System.out.println("1번행의 개수 : " + arr[1].length);
		System.out.println("2번행의 개수 : " + arr[2].length);
	}

}
